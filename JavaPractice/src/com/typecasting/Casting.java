package com.typecasting;

public class Casting {
	
	public static void main(String args[]) {
		
		//widening
		int a =10;
		double b=a;
		
		System.out.println(a);
		System.out.println(b);
		
		//narrowing
		
		 double x = 10.8;
		 int y=(int)x;
		
		 System.out.println(x);
			System.out.println(y);
			
		
			int i = 20;
			char c= (char) i;
		
			
			 System.out.println(i);
				System.out.println(c);
	}

}
