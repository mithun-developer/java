package com.conditional;

public class Condetion {
	
	public static void main(String args[]) {
		
		int marks = 100;
		
		if(marks>=80) {
			System.out.println("A Grade");
		}else if(marks>=60) {
			System.out.println("B Grade");
		}else if(marks>=40) {
			System.out.println("C Grade");
		}else {
			System.out.println("Fail");
		}
		
	}

}
