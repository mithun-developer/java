package com.programmes;

public class String_Reverse_1 {

	public static void main(String[] args) {
		
		String name ="mithun";
		char[]a = name.toCharArray();
		int size = a.length;
		
	
		
		for(int i=size-1;i>=0;i--) {
			System.out.print(a[i]);
		}
		
		
		
		
		
		
	}

}
